using System;
namespace Calculator
{
    public class Calc
    {
        public static int Add(int a, int b)
        {
            return  a + b;
        }

        public static int Sub(int a, int b)
        {
            return a - b;
        }

        public static int Mul(int a, int b)
        {
            return a * b;
        }

        public static double Div(double a, double b)
        {
            return a / b;
        }
    }
}
